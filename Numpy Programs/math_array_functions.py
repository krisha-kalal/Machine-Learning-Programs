import numpy as np 
c = np.array([1, 2, 3, 4]) # Creates a 1D array.
d = np.array([5, 6, 7, 8]) # Creates another 1D array.
print("array3:", c) 
print("array4:", d) 

print("addition:", c + d) # Adds corresponding elements of two arrays.
print("subtraction:", d - c) # Subtracts corresponding elements of one array from another.
print("multiplication:", c * d) # Multiplies corresponding elements of two arrays.
print("division:", d / c) # Divides corresponding elements of one array by another.
