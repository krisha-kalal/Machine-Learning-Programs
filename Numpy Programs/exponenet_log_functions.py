import numpy as np
arr = np.array([1, 10, 100, 1000])
log2_arr = np.log2(arr) # Compute the base-2 logarithm of each element in the array
print(f"Base-2 logarithm: {log2_arr}")

import numpy as np
log10_arr = np.log10(arr) # Compute the base-10 logarithm of each element in the array
print(f"Base-10 logarithm: {log10_arr}")

import numpy as np
exp_arr = np.exp(arr)  # Compute the exponential (e^x) of each element in the array
print(f"Exponential: {exp_arr}")

import numpy as np
log_arr = np.log(arr) # Compute the natural logarithm (base-e) of each element in the array
print(f"Natural logarithm: {log_arr}")
